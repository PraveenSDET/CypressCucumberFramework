/**
 * Retrieve element based on inner text and an identifier
 * @param element - Locator to identify the initial element
 * @param expectedText - Text value to be matched
 */
Cypress.Commands.add('getElementByText', {prevSubject: "element"}, (element, expectedText) => {
    cy.wrap(element).contains(expectedText)
})


